numero_1 = int(input("Ingrese un número: "))
numero_2 = int(input("Ingrese otro número: "))

suma = numero_1 + numero_2
resta = numero_1 - numero_2
multiplicacion = numero_1 * numero_2

print(f"Sumar: {numero_1} + {numero_2} = {suma}")
print(f"Restar: {numero_1} - {numero_2} = {resta}")
print(f"Multiplicar: {numero_1} * {numero_2} = {multiplicacion}")

print("Sumar:", numero_1, "+", numero_2, "=", suma)
print("Restar:", numero_1, "-", numero_2, "=", resta)
print("Multiplicar:", numero_1, "*", numero_2, "=", multiplicacion)
